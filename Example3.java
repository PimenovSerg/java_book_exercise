class Solution {
    public static void main(String[] args) {
        int var;
        double x;

        var = 10;

        x= 10.0;

        
        System.out.println("Начальное значение переменной var: " + var);
        System.out.println("Начальное значение переменной x: " + x);
        System.out.println();

        var = var / 4;
        x = x / 4;

        System.out.println("Начальное значение переменной var после деления: " + var);
        System.out.println("Начальное значение переменной x после деления: " + x);
    }
}